package schleifen;

public class WhileSchleife {
public static void main (String [] text) {
	byte lange, weile ; 
	lange = 1;
	weile = 7; 
	while (lange < weile) {  // geschweifte Klammern bei der(jeder) Schleife in den Klammern steht die Abbruchbedingung 
		lange ++;                    // auf deutsch bedeutet diese schleife tu ... solange das erfüllt ist 
		System.out.println("lange");
	}
		System.out.print("langeweile");
	
}
}
